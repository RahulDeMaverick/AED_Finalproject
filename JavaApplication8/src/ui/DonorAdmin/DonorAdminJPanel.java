    private void btnUpdateDonorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDonorActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex= tblDonor.getSelectedRow();
        
       if(selectedRowIndex<0){            
            return;
       }
       DefaultTableModel model= (DefaultTableModel) tblDonor.getModel();
       String insuranceNumber= (String) model.getValueAt(selectedRowIndex, 1);
       
       Donor donor = system.getDonor(insuranceNumber);
       if(isAvailablejCheckBox.isSelected()==true)
       { donor.organDonationStatus(insuranceNumber);
      
       }
       String donorName = txtdonorName.getText();
       donor.setName(donorName);
       
       String donorContact = txtdonorContact.getText();
       donor.setPhoneNumber(donorContact);
       
       String address = txtdonorAddress.getText();
       donor.setAddress(address);
       
       String email = txtdonorEmail.getText();
       donor.setEmail(email);
       
       String city = txtdonorCity.getText();
       donor.setCity(city);
       
       int age = Integer.parseInt(txtAge.getText());
       donor.setAge(age);
       
       system.updateDonor(donor);
       populateTable();
    }//GEN-LAST:event_btnUpdateDonorActionPerformed

