    public void deleteDonor()
    { 
        jdbcConnection dbconn = system.getConnection();
        Connection conn = dbconn.connect();        
        String sql = "DELETE FROM donorTable where insuranceNumber=?";
        try {
        DefaultTableModel model = (DefaultTableModel) tblDonor.getModel();
        PreparedStatement pstmt = conn.prepareStatement(sql);
        int row_val = tblDonor.getSelectedRow();
        String insuranceNumber= (String) model.getValueAt(row_val, 1);
        pstmt.setString(1,insuranceNumber);
        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(container, "The Donor has been deleted successfully");
        populateTable();
        } catch (SQLException ex) {
            Logger.getLogger(jdbcConnection.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(container, "An error has occured while deleting a donor");
        }
            finally
            {
                dbconn.disConnnect(conn);
            }



    }   

