package Business;

/**
 *
 * @author kashr
 */
public class Person {
    String name;
    String password;
    String uniqueID;

    public String getUniqueID() {
        return uniqueID;
    }

    public void setUniqueID(String uniqueID) {
        this.uniqueID = uniqueID;
    }

    public Person(String name, String password, String _ID) {
        this.name = name;
        this.password = password;
        this.uniqueID = _ID;
    }
        
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
